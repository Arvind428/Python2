"""
lists is an ordered sequence of elements that can be comprised of strings
Lists are mutable
use zero based index
"""
#index based accessing
cities=["Los Angeles","London","Tokyo"]
i=cities[0]
print(i)#Los Angeles

#negative index accessing
n=cities[-1]
print(n)#Tokyo

#list constructor
name="Arvind"
lc=list(name)
print(lc)

